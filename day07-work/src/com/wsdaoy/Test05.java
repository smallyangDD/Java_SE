package com.wsdaoy;

public class Test05 {
    public static void main(String[] args) {
        System.out.println("8的绝对值："+Math.abs(8));
        System.out.println("-8的绝对值："+Math.abs(-8));
        System.out.println("6.1的向上取整："+Math.ceil(6.1));
        System.out.println("-6.1的向上取整："+Math.ceil(-6.1));
        System.out.println("6.8的向下取整："+Math.floor(6.8));
        System.out.println("-6.8的向下取整："+Math.floor(-6.8));
        System.out.println("5.88的四舍五入："+Math.round(5.88));
        System.out.println("-5.88的四舍五入："+Math.round(-5.88));
    }
}
